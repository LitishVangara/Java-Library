package beanClassPrograms1;

public class Bank 
{
	private String name;
	private int accNo;
	private double bal;
	public Bank() {}
	public String getName() 
	{
		return name;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	public int getAccNo() 
	{
		return accNo;
	}
	public void setAccNo(int accNo) 
	{
		this.accNo = accNo;
	}
	public double getBal() 
	{
		return bal;
	}
	public void setBal(double bal) 
	{
		this.bal = bal;
	}
	
}
