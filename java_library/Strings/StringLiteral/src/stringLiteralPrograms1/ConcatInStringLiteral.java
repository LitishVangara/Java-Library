package stringLiteralPrograms1;

public class ConcatInStringLiteral 
{
	public static void main(String[] args) 
	{
		String s1 = "Hello";
		s1.concat("Hyderabad");
		System.out.println(s1);
		System.out.println("----------------------");
		String s2 = "Hello";
		s2 = s2.concat("Jspiders");
		System.out.println(s2);
	}
}
