package overrideObjectClasstoStringMethod2;

public class Student {
	public String name;
	public int id;
	public String college;
	public int tenth;
	public int inter;
	public int degree;
	public Student () {}
	public Student(String name, int id, String college, int tenth, int inter, int degree) 
	{
		this.name = name;
		this.id = id;
		this.college = college;
		this.tenth = tenth;
		this.inter = inter;
		this.degree = degree;
	}
	public String toString()
	{
		String res = "Name of the Student : "+this.name+", his Id : "+this.id+" and college Name was : "+this.college;
		return res;
	}
	
}
