package forEachLoopPrograms1;

import java.util.Enumeration;
import java.util.Vector;

public class ForEachLoopExample 
{
	public static void main(String[] args) 
	{
		Vector v = new Vector();
		v.add('a');
		v.add('e');
		v.add('i');
		v.add('o');
		v.add('u');
		System.out.println("Elements are : ");
		for (Object ch :v)
		{
			System.out.println(ch);
		}
	}
}
